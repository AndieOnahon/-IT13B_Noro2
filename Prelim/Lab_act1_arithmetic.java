package Prelim;


public class Lab_act1_arithmetic {
    public static void main (String [] args){
  
        int firstNumber = 10;
        int secondNumber = 4;
        int thirdNumber = 6;

        System.out.println(firstNumber + " + " + secondNumber + " + " + thirdNumber + " = " + (firstNumber + secondNumber + thirdNumber));
        System.out.println("(" + firstNumber + " * " + secondNumber + ") % " + thirdNumber + " = " + ((firstNumber * secondNumber) % thirdNumber));
        System.out.println(firstNumber + " * " + secondNumber + " + " + thirdNumber + " = " + (firstNumber * secondNumber + thirdNumber));
        System.out.println(firstNumber + " + " + thirdNumber + " = " + (firstNumber + thirdNumber));
        
    }
}

   

